function allStation(){
	$.ajax({
		url: '/common/allstations',
		type: 'GET',
		datatype: 'JSON',
		success: function(data){
			//$('#station_id').append('<option value="0">--station code--</option>');
			for (var i in data){
				$('#to_station').append('<option value="'+data[i].code+'">'+data[i].code+'</option>');
        	$('#from_station').append('<option value="'+data[i].code+'">'+data[i].code+'</option>');
			}
		}
	});
}
